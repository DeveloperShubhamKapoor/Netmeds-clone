function displayData(limitedPeriodData){

    limitedPeriodData.map(function(element){

    var divison = document.createElement("div")
    divison.setAttribute("class","inner-box-child")

    var span0 = document.createElement("span")
    span0.setAttribute("class","discount")
    span0.innerText = element.productDisc

    var span1 = document.createElement("span")

    var image1 = document.createElement("img")
    image1.src = element.productImg
    image1.setAttribute("class","image-set")

    var head4 = document.createElement("h4")
    head4.setAttribute("class","text-set")
    head4.innerText = element.productName

    var para1 = document.createElement("p")
    para1.setAttribute("class","cursive-set")
    para1.innerText = element.productManuf

    var span2 = document.createElement("span")
    span2.setAttribute("class","best-price")
    span2.innerText = element.productBestrice

    var span3 = document.createElement("span")
    span3.setAttribute("class","price")
    span3.innerText = element.productPrice

    var para2 = document.createElement("p")
    para2.setAttribute("class","mrp")
    para2.innerText = element.productMrp

    var strikedPrice = document.createElement("strike")
    strikedPrice.innerText = element.productStriked

    var button1 = document.createElement("button")
    button1.setAttribute("class","button")
    button1.innerText = "ADD TO CART"
    button1.addEventListener("click",function(){
        addToCart(element)
    })


    span1.append(image1)
    para2.append(strikedPrice)

    divison.append(span0,span1,head4,para1,span2,span3,para2,button1)

    document.querySelector(".inner-box").append(divison)

    });
}


